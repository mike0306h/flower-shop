export function I18nProvider({ children, initialShopInfo }) {
  const [lang, setLang] = useState('zh')
  const [shopName, setShopName] = useState(() => {
    if (initialShopInfo?.shop_name) {
      return {
        zh: initialShopInfo.shop_name,
        th: initialShopInfo.shop_name,
        en: initialShopInfo.shop_name,
      }
    }
    return {}
  })

  // 店铺名称从 props 初始化，动态更新时用 API
  useEffect(() => {
    if (!initialShopInfo?.shop_name) {
      // props 没有则从 API 读
      fetch('/api/shop-info')
        .then(r => r.json())
        .then(data => {
          if (data.shop_name) {
            setShopName({
              zh: data.shop_name,
              th: data.shop_name,
              en: data.shop_name,
            })
          }
        })
        .catch(() => {})
    }
  }, [initialShopInfo])

  const t = (key, params = {}) => {
    // shop_name 用动态值
    if (key === 'shop_name' && shopName[lang]) {
      let text = shopName[lang]
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, v)
      })
      return text
    }
    let text = translations[lang]?.[key] || translations['zh'][key] || key
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v)
    })
    return text
  }

  return (
    <I18nContext.Provider value={{ lang, setLang, t, shopName }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  return useContext(I18nContext)
}

